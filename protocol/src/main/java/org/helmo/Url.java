package org.helmo;

public class Url {
    String protocol;
    String user;
    String password;
    String host;
    String port;
    String path;

    public String getProtocol() {
        return protocol;
    }

    public String getUser() {
        return user;
    }

    public String getPassword() {
        return password;
    }

    public String getHost() {
        return host;
    }

    public String getPort() {
        return port;
    }

    public String getPath() {
        return path;
    }
}
